#loops in Python
# 1: for loop
# For loops in python are used to iterate the element of a collection in the order that they appear. This collection can be a sequence.

# Example:
sum = 0
for n in range(1,11):
    sum+= n
    print(sum)

# 2. While loop
# While loop are used to execute a number of statements or body till the specified condition is true.

# Example:

print(".")
a = 10
while a > 0:
    print("Value of a:", a)
    a = a - 1
print("Loop Terminated")

